'''-------->challenge<--------
how to reverse a string in python'''

# reverse ebircsbus to subscribe
please_sub = "ebircsbus" [::-1]
print("ebircsbus =", please_sub)

# reverse uoy evol i to i love you
i_love_u = "uoy evol i" [::-1]
print("uoy evol i =", i_love_u)

# create a function to reverse
# my crush to your crush
def my_crush(x):
    return x[::-1]
your_crush = my_crush("tfiws rolyat")
print("tfiws rolyat =", your_crush)
print("---------------------------------")

# print reversed all of the string above
print(please_sub, i_love_u, your_crush)
print("=================================")
