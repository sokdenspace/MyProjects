# Display square shape in Python

# First square shape
print("===== Square 1 (Size = 5) =====")
for i in range(5):
    for j in range(5):
        print("# ", end="")
    print()

# Second square shape
print("===== Square 2 (Size = 10) =====")
for i in range(10):
    for j in range(10):
        print("# ", end="")
    print()
